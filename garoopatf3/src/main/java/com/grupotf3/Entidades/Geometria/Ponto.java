package com.grupotf3;

public class Ponto{
    private int x, y;

    public Ponto(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX(){
        return x;
    }

    public int getX(){
        return x;
    }

    public String toString(){
        return "("+x+","+y+")";
    }
}